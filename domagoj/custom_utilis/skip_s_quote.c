#include "../../minishell.h"

int	skip_s_quote(char *str, int *c, int flag)
{
	if (str[*c] == '\'' && flag != 1)
	{
		(*c)++;
		while (str[*c] && str[*c] != '\'')
			(*c)++;
		if (str[*c] == '\'')
			(*c)++;
	}
	if (!str[*c])
		return (-1);
	return (0);
}
